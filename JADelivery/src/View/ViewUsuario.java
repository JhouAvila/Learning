    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        salvarAlterar = "alterar";
        int linha = tblUsuario.getSelectedRow();
        int codigo = 0;
        
        if(linha < 0){
            JOptionPane.showMessageDialog(this,"Selecione um usuario na tabela, antes!");
        }else{
            
            txtCod.setText(""+tblUsuario.getValueAt(linha, 0));
            txtNomeUsuario.setText(""+tblUsuario.getValueAt(linha, 1));
            txtLogin.setText(""+tblUsuario.getValueAt(linha, 2));
            nomeUsu = ""+tblUsuario.getValueAt(linha, 0);
                       
            
        }
        carregarUsuarios();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        modelUsuario = new ModelUsuario();
        modelUsuario.setNome(txtNomeUsuario.getText());
        modelUsuario.setLogin(txtLogin.getText());
        modelUsuario.setSenha(new String(txtSenha.getPassword()));
        if(salvarAlterar.equals("salvar")){        
        
            if (new String(txtSenha.getPassword()).equals(new String(txtConfirmarSenha.getPassword()))) {
                if (controllerUsuario.salvarUsuarioController(modelUsuario) > 0) {
                    carregarUsuarios();
                    limparCampos();
                    JOptionPane.showMessageDialog(this, "Item salvo!");

                }
            } else {
                JOptionPane.showMessageDialog(this, "As senhas digitadas não conferem!", "Senha!", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            if(JOptionPane.showConfirmDialog(this,"Deseja alterar o usuario '"+nomeUsu+"' ?","Alterar",
                    JOptionPane.YES_NO_OPTION)==(JOptionPane.YES_OPTION)){              
                modelUsuario.setId(Integer.parseInt(txtCod.getText()));
                carregarUsuarios();
                limparCampos();
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
         int linha = tblUsuario.getSelectedRow();
        int codigo = 0;
        if(linha < 0){
            JOptionPane.showMessageDialog(this,"Selecione um usuario na tabela, antes!");
        }else{
            codigo = (Integer) tblUsuario.getValueAt(linha, 0);
            
            if(JOptionPane.showConfirmDialog(this,"Deseja excluir o usuario '"+tblUsuario.getValueAt(linha, 1)+"' ?","Excluir",
                    JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
                controllerUsuario.excluirUsuarioController(codigo);
            }
        }
        carregarUsuarios();
        
                                              

 
        
    }//GEN-LAST:event_btnExcluirActionPerformed


    private void carregarUsuarios(){
        listaModelUsuario = controllerUsuario.retornarUsuariosController();
        DefaultTableModel modelo = (DefaultTableModel) tblUsuario.getModel();
        modelo.setNumRows(0);
        int cont = listaModelUsuario.size();
        for(int i=0;i<cont;i++){
            
            modelo.addRow(new Object[]{
                listaModelUsuario.get(i).getId(),
                listaModelUsuario.get(i).getNome(),
                listaModelUsuario.get(i).getLogin()
        });
            
        }
    }
    private void limparCampos(){
        txtNomeUsuario.setText("");
        txtLogin.setText("");
        txtSenha.setText("");
        txtConfirmarSenha.setText("");
        salvarAlterar = "salvar";
    }

